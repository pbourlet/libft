#include "libft.h"

void	ft_putnbr(int n)
{
	if (n < 0)
	{
		ft_putchar_fd('-', 1);
		if (n == -2147483648)
		{
			ft_putchar_fd('2', 1);
			n = -147483648;
		}
		n = -n;
	}
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else
		ft_putchar_fd(n + 48, 1);
}
