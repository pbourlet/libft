int		ft_testflags1(int *t, int *d, char *s, char *tab)
{
	int res;

	(d[4] == 1 && (s[t[1]] == 'x' || s[t[1]] == 'X' || s[t[1]] == 'p')
	? d[4] = 11 : 0);
	(d[4] == 1 && (s[t[1]] == 'o' || s[t[1]] == 'O') ? d[4] = 21 : 0);
	res = (d[4] == 2 && (s[t[1]] == 'o' || s[t[1]] == 'O') ? 1 : 0);
	res += t[6] - (d[4] == 11 && *tab != '0' ? 2 : 0) - (d[4] == 21
	&& *tab != '0' ? 1 : 0) - (d[2] == 1 && (s[t[1]] == 'i' || s[t[1]] == 'd'
	|| s[t[1]] == 'D') ? 1 : 0) - (d[2] == 2 && !d[3] ? 1 : 0) - (*tab == '-'
	&& (s[t[1]] == 'd' || s[t[1]] == 'i' || s[t[1]] == 'D') && t[6] >= t[8]
	? 1 : 0) - (s[t[1]] == 'p' ? 2 : 0) - (s[t[1]] == 'c' && t[7] && *tab == 0
	? 1 : 0);
	return (res);
}

void	ft_testflags2(int *t, int *d, char *s, char *tab)
{
	(d[2] == 1 && (s[t[1]] == 'i' || s[t[1]] == 'd'
	|| s[t[1]] == 'D') && tab[0] != '-' ? ft_putchar('+') : 0);
	(d[2] == 1 && (s[t[1]] == 'i' || s[t[1]] == 'd'
	|| s[t[1]] == 'D') && tab[0] != '-' ? d[0]++ : 0);
	if (t[7])
	{
		(d[2] == 2 && t[5] >= (int)ft_strlen(tab) ? ft_putchar('-') : 0);
		(d[2] == 2 && t[5] >= (int)ft_strlen(tab) ? d[0]++ : 0);
	}
	else
	{
		(d[2] == 2 && t[6] != 0 ? ft_putchar('-') : 0);
		(d[2] == 2 && t[6] != 0 ? d[0]++ : 0);
	}
	((d[4] && *tab && *tab != '0' && (s[t[1]] == 'x' || s[t[1]] == 'X'
	|| s[t[1]] == 'o' || s[t[1]] == 'O'))
	|| s[t[1]] == 'p' ? ft_putchar('0') : 0);
	((d[4] && *tab && *tab != '0' && (s[t[1]] == 'x' || s[t[1]] == 'X'
	|| s[t[1]] == 'o' || s[t[1]] == 'O')) || s[t[1]] == 'p' ? d[0]++ : 0);
	((d[4] && s[t[1]] == 'x' && *tab && *tab != '0') || s[t[1]] == 'p'
	? ft_putchar('x') : 0);
	((d[4] && s[t[1]] == 'x' && *tab && *tab != '0') || s[t[1]] == 'p'
	? d[0]++ : 0);
	(d[4] && s[t[1]] == 'X' && *tab && *tab != '0' ? ft_putchar('X') : 0);
	(d[4] && s[t[1]] == 'X' && *tab && *tab != '0' ? d[0]++ : 0);
}

int		ft_flagstock(int *d, char *s, int i)
{
	d[2] = 0;
	d[5] = 0;
	d[4] = 0;
	d[3] = 0;
	d[6] = 0;
	while (s[i] && ft_testall(s, &i) != 1 && s[i] != '%')
	{
		if (s[i] == ' ')
			d[3] = 1;
		if (s[i] == '+')
			d[2] = 1;
		if (s[i] == '-')
			d[5] = -1;
		if (s[i] == '#')
			d[4] = 1;
		if (s[i] == '0' && !(s[i - 1] >= '0' && s[i - 1] <= '9')
		&& s[i - 1] != '.')
			d[6] = 1;
		i++;
	}
	return (1);
}
